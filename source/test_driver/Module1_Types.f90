  ErrStat = ErrID_None
  ErrMsg  = ""
IF (ALLOCATED(OtherStateData%xdot)) THEN
DO i1 = LBOUND(OtherStateData%xdot,1), UBOUND(OtherStateData%xdot,1)
  CALL Mod1_DestroyContState( OtherStateData%xdot(i1), ErrStat, ErrMsg )
ENDDO
ENDIF
